#считаем площадь и длинну круга по данному радиусу

import math 

print("Эта программа подсчитает площадь и длинну окружности через данный радиус")

radius = int(input("Введите радиус окружности: "))

print("Число пи равно: ", math.pi )

pl = math.pi * (radius**2)

dlin = 2 * math.pi *  (radius)

print("Площадь данной окружности: ", pl)

print("Длина данной окружности: ", dlin)
